def is_tree(x, y, puzzle):
    puzzle_line = puzzle[y]
    puzzle_line_multiplier = math.ceil(x/len(puzzle_line)) + 1
    puzzle_line = puzzle_line * puzzle_line_multiplier
    if puzzle_line[x] == "#":
        return True
    return False


def check_pattern(x_move, y_move):
    x = 0
    y = 0
    tree_counter = 0
    for i in range(len(puzzle)-1):
        x += x_move
        y += y_move
        if y > len(puzzle):
            return tree_counter
        tree = is_tree(x, y, puzzle)
        print(x, y, tree)
        if tree:
            tree_counter += 1
    return tree_counter


